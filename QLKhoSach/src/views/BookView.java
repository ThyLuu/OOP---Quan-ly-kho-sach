    public boolean checkValidateForm() {
        if (txtID.getText().isEmpty()
                || txtName.getText().isEmpty()
                || txtAuthor.getText().isEmpty()
                || txtQuantity.getText().isEmpty()
                || txtInputDate.getText().isEmpty()) {
            return false;
        }
        return true;
    }

    public Sach getModel() throws ParseException {
        Sach b = new Sach();
        b.setMaSach(txtID.getText().trim());
        b.setTenSach(txtName.getText().trim());
        b.setTacGia(txtAuthor.getText().trim());
        b.setSoLuong(Integer.parseInt(txtQuantity.getText().trim()));
        b.setNgayNhap(date_format.parse(txtInputDate.getText().trim()));
        b.setNhaxb((NhaXB) cboNXB.getSelectedItem());
        b.setTl((TheLoai) cboGenre.getSelectedItem());
        b.setKv((KhuVuc) cboArea.getSelectedItem());
        return b;
    }

    public void fillData() {
        DefaultTableModel tbModel = (DefaultTableModel) tbBooks.getModel();
        tbModel.setRowCount(0);
        for (Sach b : daoB.getAllBook()) {
            Object[] rowData = new Object[8];
            rowData[0] = b.getMaSach();
            rowData[1] = b.getTenSach();
            rowData[2] = b.getTacGia();
            rowData[3] = b.getSoLuong();
            rowData[4] = date_format.format(b.getNgayNhap());
            rowData[5] = b.getNhaxb();
            rowData[6] = b.getTl();
            rowData[7] = b.getKv();
            tbModel.addRow(rowData);
        }
    }

    public void setModel(Sach b) {
        txtID.setText(b.getMaSach());
        txtName.setText(b.getTenSach());
        txtAuthor.setText(b.getTacGia());
        txtQuantity.setText(b.getSoLuong() + "");
        txtInputDate.setText(date_format.format(b.getNgayNhap()));
        cboNXB.setSelectedItem(b.getNhaxb());
        cboGenre.setSelectedItem(b.getTl());
        cboArea.setSelectedItem(b.getKv());
    }

    private void btnFindIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindIDActionPerformed
        // TODO add your handling code here:
        if (txtID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập mã sách cần tìm !");
        } else {
            Sach b = daoB.findByID(txtID.getText().trim());
            if (b != null) {
                lblStatus.setText("Da tim thay " + txtID.getText());
                setModel(b);
            } else {
                lblStatus.setText("Không tìm thấy " + txtID.getText());
            }
        }
    }//GEN-LAST:event_btnFindIDActionPerformed

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        // TODO add your handling code here:
        if (txtID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Hãy nhập mã sách !");
        } else {
            if (daoB.del(txtID.getText().trim()) > 0) {
                lblStatus.setText("Xoá thành công");
                fillData();
            } else {
                lblStatus.setText("Không tìm thấy mã sách ");
            }
        }
    }//GEN-LAST:event_btnDelActionPerformed

    private void tbBooksMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbBooksMouseClicked
        // TODO add your handling code here:
        int rowID = tbBooks.rowAtPoint(evt.getPoint());
        Sach b = daoB.getBookAtPoint(rowID);
        if (b == null) {
            return;
        }
        setModel(b);
        btnSave.setText("Sửa");
        position = rowID;
    }//GEN-LAST:event_tbBooksMouseClicked

    private void btnLastActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLastActionPerformed
        // TODO add your handling code here:
        position = daoB.count() - 1;
        Sach b = daoB.getBookAtPoint(position);
        setModel(b);
    }//GEN-LAST:event_btnLastActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        // TODO add your handling code here:
        position--;
        if (position <= 0) {
            position = 0;
        }
        Sach b = daoB.getBookAtPoint(position);
        setModel(b);
    }//GEN-LAST:event_btnBackActionPerformed

    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        // TODO add your handling code here:
        position++;
        if (position >= daoB.count() - 1) {
            position = daoB.count() - 1;
        }
        Sach b = daoB.getBookAtPoint(position);
        setModel(b);
    }//GEN-LAST:event_btnNextActionPerformed

