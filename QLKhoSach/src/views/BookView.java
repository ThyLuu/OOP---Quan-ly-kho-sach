    private void btnFindNameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindNameActionPerformed
        // TODO add your handling code here:
        if (txtName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập tên sách cần tìm !");
        } else {
            Sach b = daoB.findByName(txtName.getText().trim());
            if (b != null) {
                lblStatus.setText("Da tim thay " + txtName.getText());
                setModel(b);
            } else {
                lblStatus.setText("Không tìm thấy " + txtName.getText());
            }
        }
    }//GEN-LAST:event_btnFindNameActionPerformed

